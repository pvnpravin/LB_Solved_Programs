double CircleArea(float fRadius)
{
    double dArea = 0.0;

    dArea = 3.14 * fRadius * fRadius;

    return dArea;
}

///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0;
    double dRet = 0.0;

    printf("Enter The Radius..:");
    scanf("%f", &fValue);

    dRet = CircleArea(fValue);

    printf("Area of the Circle is %.4lf", dRet);

    return 0;
}