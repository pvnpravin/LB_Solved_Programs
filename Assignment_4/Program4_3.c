//
//  Function Name:  NonFact
//  Description:    It is used to Display the Non Factor of Number
//  Input:          Integer
//  Output:         Integer
//  Author:         Omkar Santosh Durge
//  Date:           27/10/2025
//
///////////////////////////////////////////////////////////////////////////////
void NonFact(int iNo)
{
    int i = 0;
    if (iNo <= 0)
    {
        iNo = -iNo;
    }
    for (i = 1; i <= iNo; i++)
    {
        if (iNo % i != 0)
        {
            printf("%d\t  ", i);
        }
    }
}

///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter the Number..\n");
    scanf("%d", &iValue);

    NonFact(iValue);

    return 0;
}