void Number(int iNo)
{
    if(iNo<50)
    {
        printf("Small..");
    }
    else if(iNo>=50 && iNo<=100)
    {
        printf("Medium..");
    }
    else
    {
        printf("Large..");
    }
}
///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the Value :");
    scanf("%d", &iValue);

    Number(iValue);
    return 0;
}