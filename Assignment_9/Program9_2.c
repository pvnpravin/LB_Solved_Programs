#include <stdio.h>
//////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  DollarToINR
//  Description:    To convert Dollar to INR
//  Input:          Integer
//  Output:         Integer
//  Author:         Omkar Santosh Durge
//  Date:           30/10/2025
//
///////////////////////////////////////////////////////////////////////////////
int DollarToINR(int iNo)
{
    int iAns = 0;
    iAns = iNo * 70;
    return iAns;
}

///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter the Value :");
    scanf("%d", &iValue);

    iRet = DollarToINR(iValue);

    printf("Value in the INR is %d", iRet);
    return 0;
}