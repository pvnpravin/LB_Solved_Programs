void CheckEvenOdd(int iNum)
{

    if (iNum % 2 == 0)
    {
        printf("%d is a Even Number", iNum);
    }
    else
    {
        printf("%d is Odd Number", iNum);
    }
}

///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int number = 0;

    printf("Enter the Number..:\n");
    scanf("%d", &number);

    CheckEvenOdd(number);

    return 0;
}