void MultiDisplay(int iNo)
{
    int iCnt = 0;
    for (iCnt = 1; iCnt <= 5; iCnt++)
    {   
       printf("%d\t",iNo*iCnt);
    }
}
///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the Value :");
    scanf("%d", &iValue);

    MultiDisplay(iValue);
    return 0;
}