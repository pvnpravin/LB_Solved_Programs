//////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  TableRev
//  Description:    To print the table of given number in Reverse Order
//  Input:          intger
//  Output:
//  Author:         Omkar Santosh Durge
//  Date:           30/10/2025
//
///////////////////////////////////////////////////////////////////////////////
void TableRev(int iNo)
{
    if (iNo < 0)
    {
        iNo = -iNo;
    }
    int iCnt = 0;
    for (iCnt = 10; iCnt >= 1; iCnt--)
    {
        printf("%d\t", iNo * iCnt);
    }
}
///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the Value :");
    scanf("%d", &iValue);

    TableRev(iValue);
    return 0;
}