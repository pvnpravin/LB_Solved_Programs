#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  Print_Numbers
//  Description:    To Print the Numbers From 1 to N
//  Input:          Integer
//  Output:
//  Author:         Pravin Ashok Chavan
//  Date:           2/11/2025
//
///////////////////////////////////////////////////////////////////////////////
void Print_Numbers(int limit)
{
    int iCnt = 0;

    for (iCnt = 1; iCnt <= limit; iCnt++)
    {
        printf("%d\t", iCnt);
    }
}

// Time Complexity =O(N)
///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLimit = 0;

    printf("Enter Number :");
    scanf("%d", &iLimit);

    Print_Numbers(iLimit);

    return 0;
}