#include <stdio.h>
//////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  FhtoCs
//  Description:    To convert Fahrenheit to Celcius
//  Input:          float
//  Output:         double
//  Author:         Omkar Santosh Durge
//  Date:           30/10/2025
//
///////////////////////////////////////////////////////////////////////////////
double FhtoCs(float fTemp)
{
    double dCel = 0.0;
    dCel = (fTemp - 32) * ((float)5/9);
    return dCel;
}

///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter Temperature in Fahrenheit..");
    scanf("%f", &fValue);

    dRet = FhtoCs(fValue);

    printf("The Celcius is :%.4lf", dRet);

    return 0;
}