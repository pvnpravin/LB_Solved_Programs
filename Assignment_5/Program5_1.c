//////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  CheckEvenOdd
//  Description:    It is used to check that number is Even or Odd
//  Input:          Integer
//  Output:
//  Author:         Omkar Santosh Durge
//  Date:           29/10/2025
//
