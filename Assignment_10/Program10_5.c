#include <stdio.h>
//////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  SquareMeter
//  Description:    To convert Square feet to Square Meter
//  Input:          float
//  Output:         double
//  Author:         Omkar Santosh Durge
//  Date:           30/10/2025
//
///////////////////////////////////////////////////////////////////////////////
double SquareMeter(int iNo)
{
    double dMeter = 0.0;
    dMeter = iNo * 0.0929;
    return dMeter;
}

///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0.0;
    double dRet = 0.0;

    printf("Enter Area in Square Feet :");
    scanf("%d", &iValue);

    dRet = SquareMeter(iValue);

    printf("The Square Meter is :%lf", dRet);

    return 0;
}