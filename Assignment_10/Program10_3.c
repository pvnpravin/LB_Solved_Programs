#include <stdio.h>
//////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  KMtoMeter
//  Description:    To convert km to meter
//  Input:          int
//  Output:         int
//  Author:         Omkar Santosh Durge
//  Date:           30/10/2025
//
///////////////////////////////////////////////////////////////////////////////
int KMtoMeter(int iNo)
{
    int iMeter = 0;
    iMeter = iNo * 1000;
    return iMeter;
}

///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    int iRet = 0.0;

    printf("Enter The Diatance:");
    scanf("%d", &iValue);

    iRet = KMtoMeter(iValue);

    printf("%d Km in Meter is %d", iValue, iRet);

    return 0;
}